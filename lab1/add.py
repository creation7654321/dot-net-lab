def add_three_numbers(a, b, c):
    return a + b + c

# Example usage
num1 = 10
num2 = 20
num3 = 30

result = add_three_numbers(num1, num2, num3)
print("The sum is:", result)


# Function to add four numbers
def add_four_numbers(a, b, c, d):
    return a + b + c + d

# Example usage
num1 = 10
num2 = 20
num3 = 30
num4 = 40

result = add_four_numbers(num1, num2, num3, num4)
print("The sum is:", result)